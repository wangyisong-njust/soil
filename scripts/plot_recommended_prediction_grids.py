ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "src"))

from soilmodel.paths import DOCS_DIR, FIGURES_DIR, TABLES_DIR, ensure_project_dirs, preferred_processed_data_path


OUT_DIR = FIGURES_DIR / "recommended_predictions"


def md_table(df: pd.DataFrame) -> str:
    if df.empty:
        return "_无记录。_"
    text_df = df.astype(str)
    lines = [
        "| " + " | ".join(text_df.columns) + " |",
        "| " + " | ".join(["---"] * len(text_df.columns)) + " |",
    ]
    for row in text_df.values.tolist():
        lines.append("| " + " | ".join(row) + " |")
    return "\n".join(lines)


def read_long(path: str) -> pd.DataFrame:
    df = pd.read_csv(ROOT / path)
    if "protocol" in df.columns:
        df = df[df["protocol"] == "temporal_2022_2026"].copy()
    return df


def spatial_quantile_prediction(target: str, method: str, model: str) -> pd.DataFrame:
    # Reuse rows generated by the final candidate builder. The prediction file
    # for spatial quantiles is implicit, so compute the selected model here.
    data = pd.read_csv(preferred_processed_data_path())
    data["year"] = data["year"].round().astype(int)
    train = data[data["year"] < 2022].copy()
    test = data[data["year"] >= 2022].copy()
    if method == "knn_spatial_quantile":
        stem, qtext = model.split("_Q")
        k = int(stem.replace("KNN", ""))
        quantile = int(qtext) / 100.0
        from sklearn.neighbors import NearestNeighbors

        nn = NearestNeighbors(n_neighbors=min(k, len(train)))
        nn.fit(train[["lon", "lat"]].to_numpy(dtype=float))
        _, idx = nn.kneighbors(test[["lon", "lat"]].to_numpy(dtype=float))
        pred = np.quantile(train[target].to_numpy(dtype=float)[idx], quantile, axis=1)
    elif method == "grid_spatial_quantile":
        stem, qtext = model.split("_Q")
        n_grid = int(stem.replace("Grid", ""))
        quantile = int(qtext) / 100.0
        lon_edges = np.linspace(float(train["lon"].min()), float(train["lon"].max()), n_grid + 1)
        lat_edges = np.linspace(float(train["lat"].min()), float(train["lat"].max()), n_grid + 1)
        train_lon_bin = np.clip(np.digitize(train["lon"], lon_edges) - 1, 0, n_grid - 1)
        train_lat_bin = np.clip(np.digitize(train["lat"], lat_edges) - 1, 0, n_grid - 1)
        test_lon_bin = np.clip(np.digitize(test["lon"], lon_edges) - 1, 0, n_grid - 1)
        test_lat_bin = np.clip(np.digitize(test["lat"], lat_edges) - 1, 0, n_grid - 1)
        train_cells = train_lon_bin.astype(str) + "_" + train_lat_bin.astype(str)
        test_cells = test_lon_bin.astype(str) + "_" + test_lat_bin.astype(str)
        cell_values = train.assign(_cell=train_cells).groupby("_cell")[target].quantile(quantile).to_dict()
        global_value = float(train[target].quantile(quantile))
        pred = np.asarray([cell_values.get(cell, global_value) for cell in test_cells], dtype=float)
    else:
        raise ValueError(f"Unsupported spatial quantile method: {method}")
    out = test[["lon", "lat", "year"]].copy()
    out["target"] = target
    out["observed"] = test[target].to_numpy(dtype=float)
    out["predicted"] = pred
    return out


def selected_prediction(row: pd.Series) -> pd.DataFrame:
    target = row["target"]
    source = row.get("source", "")
    method = row.get("method", "")
    model = row["model"]
    if source == "external_public_covariates":
        df = read_long("results/external_covariate_predictions.csv")
        part = df[
            (df["feature_set"] == "external_covariates")
            & (df["target"] == target)
            & (df["model"] == model)
        ].copy()
    elif source == "external_geo_terrain_covariates":
        df = read_long("results/unified_validation_predictions.csv")
        part = df[
            (df["regime"] == "future_year_independent_validation")
            & (df["feature_set"] == "external")
            & (df["target"] == target)
            & (df["model"] == model)
        ].copy()
    elif source == "publication_validation_fusion":
        df = read_long("results/publication_validation_fusion_predictions.csv")
        part = df[(df["target"] == target) & (df["model"] == model)].copy()
    elif source == "validation_transfer_calibration":
        df = read_long("results/validation_transfer_calibration_predictions.csv")
        part = df[(df["target"] == target) & (df["model"] == model)].copy()
        base_candidate = row.get("base_candidate", "")
        if isinstance(base_candidate, str) and base_candidate:
            part = part[part["base_candidate"] == base_candidate].copy()
    elif source == "validation_transfer_test_selected_exploration":
        df = read_long("results/validation_transfer_calibration_test_selected_predictions.csv")
        part = df[(df["target"] == target) & (df["model"] == model)].copy()
        base_candidate = row.get("base_candidate", "")
        if isinstance(base_candidate, str) and base_candidate:
            part = part[part["base_candidate"] == base_candidate].copy()
    elif source in {
        "local_analog_memory",
        "causal_history_memory",
        "arima_lstm_temporal",
        "quantile_risk_gate",
        "multi_evidence_fusion",
        "spatial_distribution_features",
        "distribution_guided_spatial_quantile",
        "spatiotemporal_innovation",
        "predefined_recent_median_baseline",
    }:
        path_map = {
            "local_analog_memory": "results/local_analog_memory_predictions.csv",
            "causal_history_memory": "results/causal_history_memory_predictions.csv",
            "arima_lstm_temporal": "results/temporal_sequence_model_predictions.csv",
            "quantile_risk_gate": "results/quantile_risk_gate_predictions.csv",
            "multi_evidence_fusion": "results/multi_evidence_fusion_predictions.csv",
            "spatial_distribution_features": "results/spatial_distribution_feature_predictions.csv",
            "distribution_guided_spatial_quantile": "results/distribution_guided_spatial_quantile_predictions.csv",
            "spatiotemporal_innovation": "results/innovation_model_predictions.csv",
            "predefined_recent_median_baseline": "results/predefined_recent_median_baseline_predictions.csv",
        }
        df = read_long(path_map[source])
        part = df[(df["target"] == target) & (df["method"] == method) & (df["model"] == model)].copy()
    elif source == "yearwise_validation_selected_publication":
        if method == "predefined_recent_center":
            df = read_long("results/predefined_recent_median_baseline_predictions.csv")
            part = df[(df["target"] == target) & (df["method"] == method) & (df["model"] == model)].copy()
        else:
            lookup = pd.read_csv(TABLES_DIR / "yearwise_validation_selected_publication_metrics.csv")
            match = lookup[(lookup["target"] == target) & (lookup["method"] == method) & (lookup["model"] == model)]
            if match.empty:
                raise ValueError(f"No yearwise selection lookup row for {target}/{method}/{model}")
            inner_source = str(match.iloc[0]["source"])
            inner = pd.Series({"target": target, "source": inner_source, "method": method, "model": model})
            part = selected_prediction(inner)
    elif source == "multitask_latent":
        df = read_long("results/multitask_latent_predictions.csv")
        part = df[df["model"] == model].copy()
        observed_col = f"observed_{target}"
        predicted_col = f"predicted_{target}"
        part = part[["lon", "lat", "year", observed_col, predicted_col]].rename(
            columns={observed_col: "observed", predicted_col: "predicted"}
        )
        part["target"] = target
    elif source in {"spatial_quantile_baseline", "spatial_quantile_validated"}:
        part = spatial_quantile_prediction(target, method, model)
    elif source == "nnls_stack_exploration":
        df = read_long("results/nnls_stack_exploration_predictions.csv")
        part = df[(df["target"] == target) & (df["model"] == model)].copy()
    elif method == "same_set_linear_stack_upper_bound":
        df = read_long("results/linear_stack_upper_bound_predictions.csv")
        part = df[(df["target"] == target) & (df["model"] == model)].copy()
    else:
        raise ValueError(f"Unsupported selected source/method: {source}/{method}/{model}")
    if part.empty:
        raise ValueError(f"No prediction rows found for {source}/{method}/{model}/{target}")
    return part[["lon", "lat", "year", "target", "observed", "predicted"]].copy()


def plot_grid(metrics: pd.DataFrame, title: str, filename: str, source_label: str) -> pd.DataFrame:
    OUT_DIR.mkdir(parents=True, exist_ok=True)
    pred_frames: list[pd.DataFrame] = []
    fig, axes = plt.subplots(2, 4, figsize=(14.5, 7.5))
    axes = axes.ravel()
    for ax, row in zip(axes, metrics.sort_values("target").itertuples(index=False)):
        row_series = pd.Series(row._asdict())
        part = selected_prediction(row_series)
        part["tier"] = source_label
        part["selected_model"] = row.model
        pred_frames.append(part)
        observed = part["observed"].to_numpy(dtype=float)
        predicted = part["predicted"].to_numpy(dtype=float)
        ax.scatter(observed, predicted, s=26, alpha=0.75, color="#4C78A8", edgecolor="white", linewidth=0.35)
        values = np.r_[observed, predicted]
        finite = values[np.isfinite(values)]
        if len(finite):
            lo, hi = float(np.min(finite)), float(np.max(finite))
            pad = (hi - lo) * 0.05 if hi > lo else 1.0
            ax.plot([lo - pad, hi + pad], [lo - pad, hi + pad], color="#333333", linewidth=0.9, linestyle="--")
            ax.set_xlim(lo - pad, hi + pad)
            ax.set_ylim(lo - pad, hi + pad)
        ax.set_title(f"{row.target} | R2={row.r2:.2f}", fontsize=10)
        ax.set_xlabel("Observed")
        ax.set_ylabel("Predicted")
        ax.grid(alpha=0.22)
    fig.suptitle(title, fontsize=14, y=1.02)
    plt.tight_layout()
    out_path = OUT_DIR / filename
    plt.savefig(out_path, dpi=300, bbox_inches="tight")
    plt.close()
    return pd.concat(pred_frames, ignore_index=True)


def main() -> None:
    ensure_project_dirs()
    publication = pd.read_csv(TABLES_DIR / "publication_grade_recommended_metrics.csv")
    exploration = pd.read_csv(TABLES_DIR / "final_adaptive_recommended_metrics.csv")
    exploration = exploration[exploration["protocol"] == "temporal_2022_2026"].copy()
    linear = pd.read_csv(TABLES_DIR / "linear_stack_upper_bound_metrics.csv")
    linear["source"] = "linear_stack_upper_bound"
    all_preds = [
        plot_grid(
            publication,
            "Publication-grade Observed vs Predicted (2022-2026)",
            "publication_grade_observed_predicted_grid.png",
            "publication_grade",
        ),
        plot_grid(
            exploration,
            "Exploration Upper Observed vs Predicted (2022-2026)",
            "nnls_exploration_observed_predicted_grid.png",
            "nnls_exploration",
        ),
        plot_grid(
            linear,
            "Linear Same-set Upper Bound Observed vs Predicted (2022-2026)",
            "linear_upper_observed_predicted_grid.png",
            "linear_same_set_upper",
        ),
    ]
    pd.concat(all_preds, ignore_index=True).to_csv(
        ROOT / "results/recommended_prediction_grid_values.csv", index=False, encoding="utf-8-sig"
    )
    report = [
        "# 推荐结果观测-预测图",
        "",
        "本报告生成三套 8 个重金属的观测-预测散点图：论文主结果、探索上限和线性同集上限。论文主结果图适合放主文或正式报告；后两套图应作为补充上限展示。",
        "",
        "- 论文主结果：`figures/recommended_predictions/publication_grade_observed_predicted_grid.png`",
        "- 探索上限：`figures/recommended_predictions/nnls_exploration_observed_predicted_grid.png`",
        "- 线性同集上限：`figures/recommended_predictions/linear_upper_observed_predicted_grid.png`",
        "",
        "合并后的绘图数据见 `results/recommended_prediction_grid_values.csv`。",
        "",
    ]
    (DOCS_DIR / "recommended_prediction_grids_report.md").write_text("\n".join(report), encoding="utf-8")
    print("Wrote recommended prediction grid outputs")


if __name__ == "__main__":
    main()
